#include "ft_pushswap.h"

int	ft_get_nb(char *str)
{
	int	nb;
	int	sign;

	nb = 0;
	sign = 1;
	while (*str)
	{
		if (*str == '+' || *str == '-')
		{
			if (*str == '-')
				sign = -1;
			str++;
		}
		while (*str >= '0' && *str <= '9')
		{
			nb = nb * 10 + (*str - '0');
			str++;
		}
	}
	return (nb * sign);
}
