#include "ft_pushswap.h"

void	ft_pb(t_tab **a, t_tab **b)
{
	t_tab	*tmp;

	write (1, "pb\n", 3);
	if ((*a) == NULL)
		return ;
	else
	{
		tmp = (*a);
		(*a) = (*a)->next;
		tmp->next = (*b);
		(*b) = tmp;
	}
}
