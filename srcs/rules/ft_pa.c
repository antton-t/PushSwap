#include "ft_pushswap.h"

void	ft_pa(t_tab **a, t_tab **b)
{
	t_tab	*tmp;

	write (1, "pa\n", 3);
	if ((*b) == NULL)
		return ;
	else
	{
		tmp = (*b);
		(*b) = (*b)->next;
		tmp->next = (*a);
		(*a) = tmp;
	}
	return ;
}
